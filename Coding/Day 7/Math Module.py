import math

x = 3
y = 4

print("Math Constants:")
print("Value of e:", math.e)
print("Value of pi:", math.pi)

print("\nTrigonometric & Conversion Functions:")
print("Sine of x (in radians):", math.sin(x))
print("x in degrees:", math.degrees(x))

print("\nPower and Root:")
print("Square root of x:", math.sqrt(x))
print("x raised to power y:", math.pow(x, y))
print("Factorial of y:", math.factorial(y))

print("\nExponential and Logarithms:")
print("e raised to x:", math.exp(x))
print("Natural log of x:", math.log(x))
print("Base-10 log of x:", math.log10(x))

print("\nRounding Functions:")
print("Ceiling of 3.3:", math.ceil(3.3))
print("Floor of 3.7:", math.floor(3.7))
